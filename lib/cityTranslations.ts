/**
 * Generates Chinese translation for a destination name in the format "州名·城市名"
 * @param cityName - English city name (e.g., "San Francisco")
 * @param stateCode - State code (e.g., "CA") or full state name
 * @returns Chinese translation in format "州名·城市名" or empty string if translation not available
 */
export function generateChineseDestinationName(cityName: string, stateCode: string): string {
  if (!cityName || !stateCode) {
    return ''
  }

  // Get full state name if abbreviation is provided
  const fullStateName = stateAbbreviations[stateCode.toUpperCase()] || stateCode

  // Get Chinese state name from translations
  const chineseStateName = translations.zh.states[fullStateName]
  if (!chineseStateName) {
    return ''
  }

  // Get Chinese city name
  const chineseCityName = cityTranslations[cityName.trim()]
  if (!chineseCityName) {
    return ''
  }

  // Return in format "州名·城市名"
  return `${chineseStateName}·${chineseCityName}`
}
